    companion object {
        fun findDecoder(id: ID): Result<Codec> {
            val codec = FFMPEG.avcodec_find_decoder(id.value)
            if (codec == MemorySegment.NULL) {
                return Result.failure(NoSuchElementException("No decoder found for codec $id"))
            }
            return Result.success(Codec(codec))
        }

        fun findEncoder(id: ID): Result<Codec> {
            val codec = FFMPEG.avcodec_find_encoder(id.value)
            if (codec == MemorySegment.NULL) {
                return Result.failure(NoSuchElementException("No encoder found for codec $id"))
            }
            return Result.success(Codec(codec))
        }
    }
